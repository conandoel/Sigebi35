    private void jtfIdEjemplarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfIdEjemplarKeyReleased
        // TODO add your handling code here:
        
        if(jtfIdEjemplar.getText().equals("")){
            
        }else{
            for(Ejemplar ejemplar:ejemplares){
                if(String.valueOf(ejemplar.getCodigo()).equals(jtfIdEjemplar.getText())){
                    jtfLibro.setText(ejemplar.getLibro().getTitulo());
                    jcbEstados.setSelectedItem(ejemplar.getEstado());
                    jtfLibro.setEditable(false);
                }
            }
        }
    }//GEN-LAST:event_jtfIdEjemplarKeyReleased

    private void jbtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnModificarActionPerformed
        // TODO add your handling code here:
        EjemplarData ed=new EjemplarData();
        Ejemplar ejemplar =new Ejemplar();
        ejemplar.setCodigo(Integer.parseInt(jtfIdEjemplar.getText()));
        ejemplar.setEstado(jcbEstados.getSelectedItem().toString());
        ed.modificarEjemplar(ejemplar);
        cargarEjemplares();
    }//GEN-LAST:event_jbtnModificarActionPerformed

    private void cargarEjemplares(){
        EjemplarData ed=new EjemplarData();
        ejemplares=ed.listarEjemplaresLibre();
    }
    
    private void cargarCombo(){
        jcbEstados.addItem("Disponible");
        jcbEstados.addItem("Prestado");
        jcbEstados.addItem("En Reparaci√≥n");
        jcbEstados.addItem("Con Demora");
    }
